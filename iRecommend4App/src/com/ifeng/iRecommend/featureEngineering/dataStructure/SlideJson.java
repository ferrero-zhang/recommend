package com.ifeng.iRecommend.featureEngineering.dataStructure;

import java.io.Serializable;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

public class SlideJson implements Serializable
{
		private static final long serialVersionUID = 1050128890144400614L;  
		@Expose
		@SerializedName("timg")
		protected String timg;			//唯一标识
		@Expose
		@SerializedName("title")
		protected String title;			//标题
		@Expose
		@SerializedName("morelink")
		protected String morelink;		//副标题
		@Expose
		@SerializedName("picheight")
		protected String picheight;		//源
		@Expose
		@SerializedName("img")
		protected String img;		//url
		@Expose
		@SerializedName("listimg")
		protected String listimg;		//关键词
		@Expose
		@SerializedName("picwidth")
		protected String picwidth;			//作者
		
		public String getTimg()
		{
			return this.timg;
		}
		public void setTimg(String timg)
		{
			this.timg = timg;
		}
		
		public String getTitle()
		{
			return this.title;
		}
		public void setTitle(String title)
		{
			this.title = title;
		}
		
		public String getMorelink()
		{
			return this.morelink;
		}
		public void setMorelink(String morelink)
		{
			this.morelink = morelink;
		}
		
		public String getPicheight()
		{
			return this.picheight;
		}
		public void setPicheight(String picheight)
		{
			this.picheight = picheight;
		}
		
		public String getImg()
		{
			return this.img;
		}
		public void setImg(String img)
		{
			this.img = img;
		}
		
		public String getListimg()
		{
			return this.listimg;
		}
		public void setListimg(String listimg)
		{
			this.listimg = listimg;
		}
		
		public String getpicwidth()
		{
			return this.picwidth;
		}
		public void setPicwidth(String picwidth)
		{
			this.picwidth = picwidth;
		}
}
