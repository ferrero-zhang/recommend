public class DocData extends Paragraph {
	
	private List<Paragraph> paras;

	public DocData() {
		super();
		paras = new ArrayList<Paragraph>();
	}

	/*
	 * 添加一个段落；
	 */
	public void addPara(Paragraph para) {
		this.paras.add(para);
	}

	public List<Paragraph> getParas() {
		return this.paras;
	}
}
